# inputs: a year (greater than zero)

# outputs: True if year is leap year, False if not

# descending if block, using and/or operators


def is_leap_year(year):
    if year % 400 == 0:
        return True
    if year % 100 == 0:
        return False
    if year % 4 == 0:
        return True
    
    return False


